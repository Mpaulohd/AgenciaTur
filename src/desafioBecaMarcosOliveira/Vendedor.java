package desafioBecaMarcosOliveira;

public class Vendedor {
	
	String nomeVendedor;
	String servicoVendido;
	double comissaoVenda;

	
	public Vendedor() {
		
	}


	public Vendedor(String nomeVendedor, String servicoVendido, double comissaoVenda) {
		super();
		this.nomeVendedor = nomeVendedor;
		this.servicoVendido = servicoVendido;
		this.comissaoVenda = comissaoVenda;
	}
	
	
}
